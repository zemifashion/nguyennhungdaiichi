                        <p><span style="color: #ff0000;"><em>➤ Chỉ với 55k/ngày Dai-ichi Việt Nam sẽ cùng bạn thực hiện những mong muốn đó thông qua GÓI BẢO HIỂM CHO MẸ VÀ BÉ, cùng với rất nhiều quyền lợi vượt qua cả mong đợi.</em></span>
                        </p>
                        <p>&nbsp;</p>
                        <hr/>
                        <h3>❀ <strong>CÁC QUYỀN LỢI CƠ BẢN BÊN TRONG GÓI BẢO HIỂM:</strong></h3><h4
                            style="padding-left: 40px;"><strong>1. Quyền lợi bảo vệ.</strong></h4>
                        <p style="padding-left: 40px;">◆ Bảo hiểm sinh mạng cho mẹ: từ <strong>800 triệu &#8211; 1,5 tỷ
                                đồng</strong></p>
                        <p style="padding-left: 40px;">◆ Thẻ chăm sóc sức khỏe toàn diện cho cả mẹ và bé, bảo vệ hai mẹ
                            con trước mọi rui ro: từ những bệnh T<strong>HÔNG THƯỜNG</strong>, đến <strong>TAI
                                NẠN</strong>, <strong>BỆNH HIỂM NGHÈO</strong> <em><span
                                    style="text-decoration: underline;">(bệnh lý nghiêm trọng)</span></em></p>
                        <p style="padding-left: 40px;">◆ Chi trả cho các vấn đề từ nhỏ nhất khi điều trị tại viện tối đa
                            lên tới 3.000.000/ngày nằm viện</p>
                        <p style="padding-left: 40px;">◆ Thanh toán <span
                                style="color: #ff0000;"><strong>toàn bộ</strong></span> chi phí thuốc chữa bệnh, các xét
                            nghiệm liên quan cho tới chi phí phẫu thuật.</p><h4 style="padding-left: 40px;"><strong>2.
                                Quyền lợi tích lũy.</strong></h4>
                        <p style="padding-left: 40px;">◈ Khách hàng có thể đóng phí <em><strong>(tùy
                                    chọn) </strong></em>15 năm hoặc 18 năm. Những năm tiếp theo cho tới năm thứ 25 của Hợp Đồng
                            không cần đóng phí &#8211; vẫn được bảo vệ như ban đầu.</p>
                        <p style="padding-left: 40px;">◈ Sau 25 năm nhận lại khoản tiền đã tiết kiệm cùng với lãi suất
                            hàng năm tại Dai-ichi từ <strong>400 triệu</strong> cho tới <strong>1 tỉ đồng.</strong> <em><strong>(tùy
                                    theo mức tiết kiệm khách hàng chọn lựa)</strong></em></p>
                        <p style="padding-left: 40px;">◈ Có thể ĐÁO HẠN SỚM từ năm thứ 10 trở đi. Nhận lại 100% giá trị
                            hoàn lại mà không phải trừ đi chi phí trước hạn.</p>
